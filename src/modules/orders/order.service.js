import CustomError from "#shared/errors/CustomError.util.js";
import crypto from "crypto";

export const createOrderService = async ({ userId, productData }) => {
  const { products, amount, tax, shipping, currency } = productData;

  const options = {
    amount: Math.round(Number(amount) * 100), // Amount in paise
    currency: currency || "INR",
    receipt: `receipt_${Date.now()}`,
  };

  const razorpayOrder = await razorpayInstance.orders.create(options);

  //save order in DB
  const newOrder = new Order({
    userId: userId,
    products,
    amount,
    tax,
    shipping,
    currency,
    status: "pending",
    razorpayOrderId: razorpayOrder.id,
  });
  await newOrder.save();

  return razorpayOrder;
};

export const verifyPaymentService = async ({ userId, razorpayData }) => {
  const {
    razorpay_order_id,
    razorpay_payment_id,
    razorpay_signature,
    paymentFailed,
  } = razorpayData;

  if (paymentFailed) {
    const order = await Order.findOneAndUpdate(
      { razorpayOrderId: razorpay_order_id },
      { status: "failed" },
      { new: true },
    );

    // throw new CustomError(
    //   "Payment failed and order status updated successfully!",
    //   400,
    // );
    return {
      order,
    };
  }

  const expectedSignature = crypto
    .createHmac("sha256", process.env.RAZORPAY_KEY_SECRET)
    //signature is generated by concatenating order_id and payment_id with a pipe (|) separator
    .update(razorpay_order_id + "|" + razorpay_payment_id)
    .digest("hex");

  if (expectedSignature !== razorpay_signature) {
    await Order.findOneAndUpdate(
      { razorpayOrderId: razorpay_order_id },
      { status: "failed" },
      { new: true },
    );

    throw new CustomError(
      "Payment verification failed due to signature mismatch",
      400,
    );
  }

  const order = await Order.findOneAndUpdate(
    { razorpayOrderId: razorpay_order_id },
    {
      status: "paid",
      razorpayPaymentId: razorpay_payment_id,
      razorpaySignature: razorpay_signature,
    },
    { new: true },
  );
  await Cart.findOneAndDelete(
    { userId },
    { $set: { items: [], totalPrice: 0 } },
  );

  return order;
};

