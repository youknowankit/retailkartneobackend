export const getSalesDataService = async () => {
  const totalUsers = await User.countDocuments({});
  const totalProducts = await Product.countDocuments({});
  const totalOrders = await Order.countDocuments({ status: "paid" });

  //Total Sales Amount
  const totalSaleAgg = await Order.aggregate([
    { $match: { status: "paid" } },
    { $group: { _id: null, total: { $sum: "$amount" } } },
  ]);

  const totalSales = totalSaleAgg[0]?.total || 0;

  //Sales grouped by date (last 30 days)
  const thirtyDaysAgo = new Date();
  thirtyDaysAgo.setDate(thirtyDaysAgo.getDate() - 30);

  const salesByDate = await Order.aggregate([
    { $match: { status: "paid", createdAt: { $gte: thirtyDaysAgo } } },
    {
      $group: {
        _id: { $dateToString: { format: "%Y-%m-%d", date: "$createdAt" } },
        amount: { $sum: "$amount" },
      },
    },
    { $sort: { _id: 1 } }, // Sort by date ascending
  ]);

  const formattedSales = salesByDate.map((item) => ({
    date: item._id,
    amount: item.amount,
  }));

  return { totalUsers, totalProducts, totalOrders, totalSales, formattedSales };
};
